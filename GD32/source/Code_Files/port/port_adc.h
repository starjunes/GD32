#ifndef PORT_ADC_H
#define PORT_ADC_H          1

/*
********************************************************************************
*                   数据类型和宏定义
********************************************************************************
*/
// 定义ADC通道号
typedef enum {
    ADC_MAINPWR,
    ADC_BAKPWR,
    ADC_ACCPWR,//ACC信号
    MAX_ADC_CHAN
} ADC_CHAN_E;
/*
********************************************************************************
*                   对外接口声明
********************************************************************************
*/
/*******************************************************************************
** 函数名:     PORT_GetADCValue
** 函数描述:   获取指定通道的AD值
** 参数:       [in] ch： ADC通道号
** 返回:       实时采集的AD值; -1表示无效
*******************************************************************************/
INT32U PORT_GetADCValue(ADC_CHAN_E ch);

/*******************************************************************************
** 函数名:     PORT_CloseADC
** 函数描述:   关闭ADC模块
** 参数:       无
** 返回:       无
*******************************************************************************/
void PORT_CloseADC(void);

/*******************************************************************************
** 函数名:     PORT_ResetADC
** 函数描述:   复位ADC模块，并重新初始化配置
** 参数:       无
** 返回:       无
*******************************************************************************/
void PORT_ResetADC(void);

//-------------------------------------------------------------------------------
#endif /* PORT_ADC_H */

