#ifndef PORT_DM_H
#define PORT_DM_H   1

/*
********************************************************************************
*                   模块数据类型及宏定义
********************************************************************************
*/
#define YX_MemMalloc(SIZE)  PORT_Malloc(SIZE)
#define YX_MemFree(PTR)     PORT_Free(PTR)
/*
********************************************************************************
*                   对外接口声明
********************************************************************************
*/
/********************************************************************************
** 函数名:     PORT_Malloc
** 函数描述:   申请指定大小的动态内存
** 参数:       [in] size: 申请动态内存的大小
** 返回:       申请成功返回内存指针，失败则返回NULL
********************************************************************************/
void *PORT_Malloc(INT32U size);

/********************************************************************************
** 函数名:     PORT_Free
** 函数描述:   释放动态内存
** 参数:       [in] ptr: 待释放的动态内存指针
** 返回:       无
********************************************************************************/
void PORT_Free(void * ptr);

/*****************************************************************************
**  函数名:  PORT_GetFreeHeapSize
**  函数描述: 获取动态内存动态空间
**  参数:    无
**  返回:    动态内容空闲空间
*****************************************************************************/
INT32U PORT_GetFreeHeapSize(void);

/*****************************************************************************
**  函数名:  PORT_GetDmSize
**  函数描述: 获取已经使用的内存大小
**  参数:    无
**  返回:    已经使用的内存大小
*****************************************************************************/
INT32U PORT_GetDmSize(void);

/*****************************************************************************
**  函数名:  PORT_GetDmBlocks
**  函数描述: 获取内存块数量
**  参数:    无
**  返回:    动态内存块数量
*****************************************************************************/
INT32U PORT_GetDmBlocks(void);

//-------------------------------------------------------------------------------
#endif /* PORT_DM_H */



