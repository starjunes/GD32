/*****************************************************************************
**  函数名:  YX_UDS_RoutineControl
**  函数描述: 历程控制服务(0x31)
**  参数:    [in] sid  : 历程控制sid
**           [in] data : 数据
**           [in] len  : 数据长度
**  返回:    无
*****************************************************************************/
