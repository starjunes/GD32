/*****************************************************************************
**  函数名:  YX_UDS_ReqDownload
**  函数描述: 请求下载服务(0x34)
**  参数:    [in] sid  : 请求下载sid
**           [in] data : 数据
**           [in] len  : 数据长度
**  返回:    无
*****************************************************************************/
