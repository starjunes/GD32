/*****************************************************************************
**  函数名:  YX_UDS_TransData
**  函数描述: 传输数据服务(0x36)
**  参数:    [in] sid  : 传输数据sid
**           [in] data : 数据
**           [in] len  : 数据长度
**  返回:    无
*****************************************************************************/
void YX_UDS_TransData(INT8U sid, INT8U* data, INT16U len);

/*****************************************************************************
**  函数名:  YX_UDS_ReqTransExit
**  函数描述: 请求传输退出服务(0x37)
**  参数:    [in] sid  : 请求传输退出sid
**  返回:    无
*****************************************************************************/
