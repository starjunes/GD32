#ifndef YX_VERSION_H
#define YX_VERSION_H    1

#include "bal_version.h"

/*
********************************************************************************
*                   数据类型及宏定义
********************************************************************************
*/
//#define MAX_VERSION_LEN        30
#define APPDAL_VERSION_STR     "c1775002t15-ae64gdm4-001"
#define APP_VERSION_NUM        APPDAL_VERSION_STR
#define APP_VERSION_TYPE       "ae64gdm4"

#define CLIENT_VERSION_STR	   "Y4.35.2006"
#define CHANGCHUN              "Y"           /* 长春 */
#define QINGDAO                "V"           /* 青岛 */
/*
********************************************************************************
*                   对外接口声明
********************************************************************************
*/
/********************************************************************************
*    函 数 名： YX_GetVersionDate
*    功能说明： 获取生成当前版本的日期信息
*    输入参数： 无
*    返回:      日期信息字符串指针    
********************************************************************************/
char* YX_GetVersionDate(void);

/********************************************************************************
*    函 数 名： YX_GetVersionTime
*    功能说明： 获取生成当前版本的时间信息
*    输入参数： 无
*    返回:     时间信息字符串指针
********************************************************************************/
char* YX_GetVersionTime(void);

/********************************************************************************
*    函 数 名： YX_GetClientVersion
*    功能说明： 获取客户版本号
*    输入参数： 无
*    返回:      版本号字符串指针
********************************************************************************/
char *YX_GetClientVersion(void);


/********************************************************************************
*    函 数 名： YX_GetVersion
*    功能说明： 获取当前程序版本号(含APP/DAL层和HAL层)
*    输入参数： 无
*    返回:      版本号字符串指针    
********************************************************************************/
char *YX_GetVersion(void);

/********************************************************************************
*    函 数 名： YX_GetVersion_EXT
*    功能说明： 获取当前程序版本号，加上日期和时间显示
*    输入参数： 无
*    返回:     版本号字符串指针
********************************************************************************/
char* YX_GetVersion_EXT(void);

//-------------------------------------------------------------------------------
#endif /* YX_VERSION_H */
