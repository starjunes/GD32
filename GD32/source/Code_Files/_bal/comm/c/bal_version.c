#include "yx_includes.h"

/*
********************************************************************************
*                   定义模块数据类型、变量及宏
********************************************************************************
*/
#define DAL_VERSION_STR     "dal-1.00"

/********************************************************************************
**  函数名:     bal_GetDalLibVersion
**  函数描述:   获取DAL库版本
**  参数:       无
**  返回:       返回库版本指针
********************************************************************************/
char *bal_GetDalLibVersion(void)
{
    return (DAL_VERSION_STR"-"__DATE__"-"__TIME__);
}

