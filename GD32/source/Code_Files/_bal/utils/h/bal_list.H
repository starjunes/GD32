#ifndef DEF_LIST
#define DEF_LIST

#define LISTMEM     INT8U
#define LISTNODE    struct node

typedef struct node {
    LISTNODE  *prv;
    LISTNODE  *next;
} NODE_T;

typedef struct {
    LISTNODE  *Head;
    LISTNODE  *Tail;
    INT32U     Item;
} LIST_T;



/*******************************************************************
** 函数名:     bal_CheckList
** 函数描述:   检测链表的有效性
** 参数:       [in]  Lp:        链表
** 返回:       true:    链表有效
**             false:   链表无效
********************************************************************/
//BOOLEAN bal_CheckList(LIST_T *Lp);
BOOLEAN bal_CheckList(LIST_T *Lp, void *bptr, void *eptr, BOOLEAN needchecklp);

/*******************************************************************
** 函数名:     bal_InitList
** 函数描述:   初始化链表
** 参数:       [in]  Lp:        链表
** 返回:       true:    成功
**             false:   失败
********************************************************************/
BOOLEAN bal_InitList(LIST_T *Lp);

/*******************************************************************
** 函数名:     bal_ListItem
** 函数描述:   获取链表节点个数
** 参数:       [in]  Lp:        链表
** 返回:       链表节点个数
********************************************************************/
INT32U bal_ListItem(LIST_T *Lp);

/*******************************************************************
** 函数名:     bal_GetListHead
** 函数描述:   获取链表头节点
** 参数:       [in]  Lp:        链表
** 返回:       链表头节点; 如链表无节点, 则返回0
********************************************************************/
LISTMEM *bal_GetListHead(LIST_T *Lp);

/*******************************************************************
** 函数名:     bal_GetListTail
** 函数描述:   获取链表尾节点
** 参数:       [in]  Lp:        链表
** 返回:       链表尾节点; 如链表无节点, 则返回0
********************************************************************/
LISTMEM *bal_GetListTail(LIST_T *Lp);

/*******************************************************************
** 函数名:     bal_ListNextEle
** 函数描述:   获取指定节点的后一节点
** 参数:       [in]  Bp:        链表当前节点
** 返回:       Bp的下一个节点; 如返回0, 则表示节点不存在
********************************************************************/
LISTMEM *bal_ListNextEle(LISTMEM *Bp);

/*******************************************************************
** 函数名:     bal_ListPrvEle
** 函数描述:   获取指定节点的前一节点
** 参数:       [in]  Bp:        指定节点
** 返回:       返回指定节点Bp的前一节点; 如返回0, 则表示不存在前一节点
********************************************************************/
LISTMEM *bal_ListPrvEle(LISTMEM *Bp);

/*******************************************************************
** 函数名:     bal_DelListEle
** 函数描述:   删除指定节点
** 参数:       [in]  Lp:        链表
**             [in]  Bp:        指定节点
** 返回:       返回指定节点Bp的下个节点; 如返回0, 则表示Bp不存在下一节点
********************************************************************/
LISTMEM *bal_DelListEle(LIST_T *Lp, LISTMEM *Bp);

/*******************************************************************
** 函数名:     bal_DelListHead
** 函数描述:   删除链表头节点
** 参数:       [in]  Lp:        链表
** 返回:       链表头节点; 如返回0, 则表示不存在链表头节点
********************************************************************/
LISTMEM *bal_DelListHead(LIST_T *Lp);

/*******************************************************************
** 函数名:     bal_DelListTail
** 函数描述:   删除链表尾节点
** 参数:       [in]  Lp:        链表
** 返回:       链表尾节点; 如返回0, 则表示不存在链表尾节点
********************************************************************/
LISTMEM *bal_DelListTail(LIST_T *Lp);

/*******************************************************************
** 函数名:     bal_AppendListEle
** 函数描述:   在链表尾上追加一个节点
** 参数:       [in]  Lp:        链表
**             [in]  Bp:        待追加节点
** 返回:       追加成功或失败
********************************************************************/
BOOLEAN bal_AppendListEle(LIST_T *Lp, LISTMEM *Bp);

/*******************************************************************
** 函数名:     bal_InsertListHead
** 函数描述:   在链表头插入一个节点
** 参数:       [in]  Lp:        链表
**             [in]  Bp:        待插入的节点
** 返回:       插入成功或失败
********************************************************************/
BOOLEAN bal_InsertListHead(LIST_T *Lp, LISTMEM *Bp);

/*******************************************************************
** 函数名:     bal_ConnectHeadTail
** 函数描述:   将list首尾相连,形成环形
** 参数:       [in]  Lp:        链表
** 返回:       连接成功或失败
********************************************************************/
BOOLEAN bal_ConnectHeadTail(LIST_T *Lp);

/*******************************************************************
** 函数名:     bal_BInsertListEle
** 函数描述:   在指定节点前插入一个新节点
** 参数:       [in]  Lp:        链表
**             [in]  CurBp:     指定节点
**             [in]  InsBp:     待插入节点
** 返回:       插入成功或失败
********************************************************************/
BOOLEAN bal_BInsertListEle(LIST_T *Lp, LISTMEM *CurBp, LISTMEM *InsBp);

/*******************************************************************
** 函数名:     bal_AInsertListEle
** 函数描述:   在指定节点后插入一个新节点
** 参数:       [in]  Lp:        链表
**             [in]  CurBp:     指定节点
**             [in]  InsBp:     待插入节点
** 返回:       插入成功或失败
********************************************************************/
BOOLEAN bal_AInsertListEle(LIST_T *Lp, LISTMEM *CurBp, LISTMEM *InsBp);

/*******************************************************************
** 函数名:     bal_InitMemList
** 函数描述:   将一块内存初始化成链表缓冲区
** 参数:       [in]  memLp:     链表
**             [in]  addr:      内存起始地址
**             [in]  nblks:     内存块个数
**             [in]  blksize:   内存块大小
** 返回:       成功或失败
********************************************************************/
BOOLEAN bal_InitMemList(LIST_T *memLp, LISTMEM *addr, INT32U nblks, INT32U blksize);

#endif
