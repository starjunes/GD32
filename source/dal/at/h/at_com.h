#ifndef AT_COM_H
#define AT_COM_H            1


/*******************************************************************
** 函数名:     AT_COM_PullUp
** 函数描述:   打开电源
** 参数:       无
** 返回:       无
********************************************************************/
void AT_COM_PullUp(void);

/*******************************************************************
** 函数名:     AT_COM_PullDown
** 函数描述:   关掉电源
** 参数:       无
** 返回:       无
********************************************************************/
void AT_COM_PullDown(void);

/*******************************************************************
** 函数名:     AT_COM_OpenGsm
** 函数描述:   开机
** 参数:       无
** 返回:       无
********************************************************************/
void AT_COM_OpenGsm(void);

/*******************************************************************
** 函数名:     AT_COM_Init
** 函数描述:   初始化函数
** 参数:       无
** 返回:       无
********************************************************************/
void AT_COM_Init(void);

/*******************************************************************
** 函数名:     AT_COM_SetBaud
** 函数描述:   初始化串口波特率
** 参数:       [in] baud:波特率1200-115200
** 返回:       无
********************************************************************/
void AT_COM_SetBaud(INT32U baud);

/*******************************************************************
** 函数名:     AT_COM_CloseUart
** 函数描述:   关闭串口
** 参数:       无
** 返回:       无
********************************************************************/
void AT_COM_CloseUart(void);

/*******************************************************************
** 函数名:     AT_COM_ReadByte
** 函数描述:   通道数据接收接口
** 参数:       无
** 返回:       数据，无数据返回－1
********************************************************************/
INT32S AT_COM_ReadByte(void);

/*******************************************************************
** 函数名:     AT_COM_SendData
** 函数描述:   发送数据
** 参数:       [in] ptr: 数据指针
**              [in] len: 数据长度
** 返回:       成功返回true，失败返回false
********************************************************************/
BOOLEAN AT_COM_SendData(INT8U *ptr, INT16U len);



#endif
